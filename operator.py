# Arithmetic Operator

#Penjumlahan
print(1 + 1) # 2
#Pengurangan
print(2 - 1) # 1
#Perkalian
print(2 * 2) # 4
#Pembagian
print(4 / 2) # 2 Mengambil nilai float (koma)
#Sisa Bagi
print(11 % 2) # 1
#Pangkat
print(8 ** 2) # 64, 8 dikali 8
#Pembagian Bulat
print(10 // 3) # 3, Nilai sisa tidak dianggap / dibulatkan

# Operator Perbandingan (Comparasion)
# Hasil dari operator perbandingan akan bersifat Boolean

#Equal
print(1 == 1) # True, satu sama dengan satu
print(1 == 2) # False, satu tidak sama dengan dua

# NOT Equal
print(2 != 2) # False, dua sama dengan dua
print(2 != 3) # True, dua tidak sama denagn tiga

# Bigger than
print(5 > 2) # True, lima lebih besar dari dua

# Smaller than
print(5 < 2) # False, lima tidak lebih kecil dari dua

# Bigger than equal
print(5 >= 2) # True, lima lebih besar dan sama dengan dari dua

# Smaller than equal
print(5 <= 2) # False, lima tidak lebih kecil dan sama dengan dari dua

# Assigment Operator
# Disebut juga operator penugasan, untuk mempersingkat suatu operasi sederhana

# Assignment
a = 1
print(a) # 1

# Plus Assigment
a += 2
print(a) # 3, 1 + 2 = 3

b = 2
# Minus Assigment
b -= 1
print(b) # 1, 2 - 1 = 1

c = 2
# Perkalian
c *= 2 
print(c) # 4, 2 * 2 = 4

# Pembagian
c /= 2
print(c) # 2, 4 / 2 = 2 (Mengambil hasil float, untuk pembagian)