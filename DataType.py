# Boolen type
print(True)
print(False)

# String type
print("Budi")
print('Hello World')

# Integer type
print(100)
print(100.000)

# Float type
print(3.14)
print(0.33)

# Hexadecimal
print(0x9a)

# Complex
print(5j)

# List: Data untaian yang menyimpan berbagai tipe data dan isinya bisa diubah-ubah
print([True, 'xyz', 789, 3.14])

# Tuple: Data untaian yang menyimpan berbagai tipe data dan isinya tidak bisa diubah-ubah
print((True, 'xyz', 789, 3.14))

# Set: Kumpulan data unik yang tidak berurutan dan tidak bisa memiliki duplikat
print({'Apel', 'Jeruk'})

# Dictionary: Data untaian yang menyimpan berbagai tipe data berupa pasangan penunjuk dan nilai
print({'Nama': 'Adi', 'id': 2})